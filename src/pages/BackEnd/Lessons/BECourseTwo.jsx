import { useEffect, useState } from "react";
import { useNavigate } from 'react-router-dom';

import "../../../styles/App.css";

import { Lesson } from "../../../components/Lesson";
import { Header } from "../../../components/Header";

export function BECourseTwo() {
  const [lessons, setLessons] = useState([]);
  const navigate = useNavigate();

  useEffect(() => {
    fetch(
      "https://frontend-project.staart.com/lessons/323ae098-1624-4e24-8607-1f3a04718b11"
    ).then(async (response) => {
      const body = await response.json();
      setLessons(
        body.modules.map((lesson) => ({
          title: lesson.title,
          lesson: lesson.lessons.map((value) => (
            <div key={value.title} className="container-class">
              {value.title}
            </div>
          )),
        }))
      );
    });
  }, []);

  function handleButton() {
    navigate('/journey/eca8e48a-57b3-46a1-a6ae-923aca77fc11')
  }

  return (
    <div className="wrapper-classes">
      <Header onClick={handleButton} id="back" title="<< Voltar" />
      <h2>Suas Primeiras Linhas de Código (JS)</h2>
      <h3>
        Que tal aprender a programar hoje mesmo? A lógica de programação é o
        primeiro passo para alcançar esse objetivo e faz mais parte do seu dia a
        dia do que você imagina. Letícia Tiveron, Engenheira de Software da Meta
        (Facebook), apresentará o curso de lógica de programação em JavaScript e
        lhe guiará pelas suas primeiras linhas de código.
      </h3>
      {lessons.map((lesson) => (
        <Lesson key={lesson.title} module={lesson.title} title={lesson.lesson} />
      ))}
    </div>
  );
}
